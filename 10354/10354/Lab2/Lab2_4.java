public class Lab2_4 {
    public static void main( String args[] ){
        System.out.println( 123 );
        System.out.println( 123L );
        System.out.println( 0123 ); //ฐาน8
        System.out.println( 0x123 ); //ฐาน 16
        System.out.println( 123.50 ); 
        System.out.println( 123.5f );
        System.out.println( 123e3 ); //ex+
        System.out.println( 123e-2 ); // ex-
    }
}
    
 
 
